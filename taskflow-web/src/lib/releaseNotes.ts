export type ReleaseNote = {
  date: string;
  title: string;
  summary: string;
  details: string[];
};

export type ReleaseNoteGroup = {
  date: string;
  notes: ReleaseNote[];
};

export function groupReleaseNotesByDate(notes: ReleaseNote[]) {
  return notes.reduce<ReleaseNoteGroup[]>((groups, note) => {
    const currentGroup = groups[groups.length - 1];
    if (currentGroup?.date === note.date) {
      currentGroup.notes.push(note);
      return groups;
    }

    groups.push({ date: note.date, notes: [note] });
    return groups;
  }, []);
}

